// JavaScript variables

/*
Variables can be declared in 4 ways

1. Automatically
2. Using var
3. Using let
4. Using const

*/


//Automatically
x = 5;
y = 10;
z= x + y;

console.log("Automatically declared variable total", z)

//Using var
var a = 3;
var b = 7;
var c = a + b;

console.log("Declare using var total is ", c)

//Using let
let m = 13;
let n = 67;
let o = m + n;

console.log("Declare using let total is ", o)


//Using const
const u = 8;
const v = 19;
const w = u + v;

console.log("Declare using const total is ", w)


//Mixed example
const apple = 5;
const mango = 8;

let total = apple + mango;

console.log("Mixed example total ", total)


